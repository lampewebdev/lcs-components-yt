<template>
  <button  
    class="lcs-button"
    :class="type" 
    :style="{ display: display, width: width }"
  >
    <slot />
  </button>
</template>

<script lang="ts">
import { defineComponent, Ref, ref } from "vue";
export default defineComponent({
  name: "LcsButton",
  props: {
    type:{
      type: String,
      default: "primary",
      validator: (value: string) => ['primary', 'plain', 'full'].includes(value)
    },
    block: {
      type: Boolean,
      default: false
    }
  },
  setup(props) {
    let display: Ref<string> = ref('inline-block')
    let width: Ref<string> = ref('unset')

    if(props.block) {
      display.value = 'block';
      width.value = "100%";
    }

    return {
      display,
      width
    }
  },
});
</script>

